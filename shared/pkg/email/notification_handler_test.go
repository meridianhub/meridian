// mockOutboxRepository implements email.OutboxRepository for testing.
type mockOutboxRepository struct {
	enqueueFunc                       func(ctx context.Context, entry *email.OutboxEntry) error
	cancelByIdempotencyKeyPatternFunc func(ctx context.Context, pattern string) (int64, error)
	enqueuedEntries                   []*email.OutboxEntry
}

func (m *mockOutboxRepository) Enqueue(ctx context.Context, entry *email.OutboxEntry) error {
	m.enqueuedEntries = append(m.enqueuedEntries, entry)
	if m.enqueueFunc != nil {
		return m.enqueueFunc(ctx, entry)
	}
	entry.ID = uuid.New()
	return nil
}

func (m *mockOutboxRepository) FetchDispatchable(_ context.Context, _ int) ([]email.OutboxEntry, error) {
	return nil, nil
}

func (m *mockOutboxRepository) MarkSent(_ context.Context, _ uuid.UUID) error { return nil }
func (m *mockOutboxRepository) MarkFailed(_ context.Context, _ uuid.UUID, _ string) error {
	return nil
}
func (m *mockOutboxRepository) Cancel(_ context.Context, _ uuid.UUID) error { return nil }

func (m *mockOutboxRepository) CancelByIdempotencyKeyPattern(ctx context.Context, pattern string) (int64, error) {
	if m.cancelByIdempotencyKeyPatternFunc != nil {
		return m.cancelByIdempotencyKeyPatternFunc(ctx, pattern)
	}
	return 0, nil
}

// mockEmailResolver implements email.PartyEmailResolver for testing.
type mockEmailResolver struct {
	resolveFunc func(ctx context.Context, partyID string) (string, error)
}

func (m *mockEmailResolver) ResolveEmail(ctx context.Context, partyID string) (string, error) {
	if m.resolveFunc != nil {
		return m.resolveFunc(ctx, partyID)
	}
	return partyID + "@example.com", nil
}

func newTestStarlarkContext() *saga.StarlarkContext {
	return &saga.StarlarkContext{
		Context:         context.Background(),
		SagaExecutionID: uuid.New(),
		IdempotencyKey:  "step_1",
		Logger:          slog.Default(),
	}
}

func TestNotificationSendHandler_EmailType(t *testing.T) {
	outbox := &mockOutboxRepository{}
	resolver := &mockEmailResolver{}
	handler := email.NewNotificationSendHandler(email.NotificationHandlerDeps{
		Outbox:        outbox,
		EmailResolver: resolver,
		Logger:        slog.Default(),
	})

	ctx := newTestStarlarkContext()
	result, err := handler(ctx, map[string]any{
		"type":      "EMAIL",
		"recipient": "party-123",
		"template":  "dunning-notice",
		"data":      map[string]any{"severity": 1},
	})

	require.NoError(t, err)
	resultMap, ok := result.(map[string]any)
	require.True(t, ok)
	assert.Equal(t, "QUEUED", resultMap["status"])
	assert.NotEmpty(t, resultMap["outbox_id"])
	assert.NotEmpty(t, resultMap["idempotency_key"])

	// Verify outbox entry
	require.Len(t, outbox.enqueuedEntries, 1)
	entry := outbox.enqueuedEntries[0]
	assert.Equal(t, []string{"party-123@example.com"}, entry.ToAddresses)
	assert.Equal(t, "dunning-notice", entry.TemplateName)
	assert.Equal(t, 1, entry.TemplateData["severity"])
}

