func TestNotificationSendHandler_SubjectGeneration(t *testing.T) {
	tests := []struct {
		name     string
		template string
		data     map[string]any
		expected string
	}{
		{
			name:     "dunning notice",
			template: "dunning-notice",
			data:     map[string]any{"severity": 2},
			expected: "Payment reminder - severity 2",
		},
		{
			name:     "account frozen",
			template: "account-frozen",
			data:     nil,
			expected: "Your account has been frozen",
		},
		{
			name:     "generic template",
			template: "custom-template",
			data:     nil,
			expected: "Notification: custom-template",
		},
	}

	for _, tc := range tests {
		t.Run(tc.name, func(t *testing.T) {
			outbox := &mockOutboxRepository{}
			handler := email.NewNotificationSendHandler(email.NotificationHandlerDeps{
				Outbox:        outbox,
				EmailResolver: &mockEmailResolver{},
				Logger:        slog.Default(),
			})

			params := map[string]any{
				"type":      "EMAIL",
				"recipient": "party-123",
				"template":  tc.template,
			}
			if tc.data != nil {
				params["data"] = tc.data
			}

			_, err := handler(newTestStarlarkContext(), params)
			require.NoError(t, err)
			require.Len(t, outbox.enqueuedEntries, 1)
			assert.Equal(t, tc.expected, outbox.enqueuedEntries[0].Subject)
		})
	}
}
