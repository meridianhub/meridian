func TestNotificationSendHandler_DuplicateIdempotencyIsNotError(t *testing.T) {
	outboxID := uuid.New()
	outbox := &mockOutboxRepository{
		enqueueFunc: func(_ context.Context, entry *email.OutboxEntry) error {
			entry.ID = outboxID
			return fmt.Errorf("%w: %q", email.ErrDuplicateIdempotency, entry.IdempotencyKey)
		},
	}
	handler := email.NewNotificationSendHandler(email.NotificationHandlerDeps{
		Outbox:        outbox,
		EmailResolver: &mockEmailResolver{},
		Logger:        slog.Default(),
	})

	ctx := newTestStarlarkContext()
	result, err := handler(ctx, map[string]any{
		"type":      "EMAIL",
		"recipient": "party-123",
	})

	require.NoError(t, err)
	resultMap := result.(map[string]any)
	assert.Equal(t, "QUEUED", resultMap["status"])
	assert.Equal(t, true, resultMap["replay"])
}

